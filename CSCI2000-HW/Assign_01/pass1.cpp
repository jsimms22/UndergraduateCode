#include <iostream>

using namespace std;

int main ()
{
    cout << "A mathematician and an engineer are sitting at a table" << endl;
    cout << "drinking when a very beautiful woman walks in and sits" << endl;
    cout << "down at the bar." << endl;
    cout << endl;
    cout << "The mathematician sighs. 'I'd love to talk to her, but" << endl;
    cout << "I can only cover half the distance between where we" << endl;
    cout << "are and her, then half the distance again, and so on." << endl;
    cout << "But, alas there will always be space between us." << endl;
    cout << endl;
    cout << "The engineer looks at the mathematician, and says" << endl;
    cout << "'eh, I bet I'll get close enough to not matter' then" << endl;
    cout << "walks over and sits next to the woman." << endl;

    return 0;
}
